#include "asm_reg.h"

.macro push_ctx
        addi sp, sp, -140 // 35 * 4

        sw x1, STACK_OFFSET_RA(sp)
        sw x3, STACK_OFFSET_GP(sp)
        sw x5, STACK_OFFSET_T0(sp)
        sw x6, STACK_OFFSET_T1(sp)
        sw x7, STACK_OFFSET_T2(sp)
        sw x8, STACK_OFFSET_S0(sp)
        sw x9, STACK_OFFSET_S1(sp)
        sw x10, STACK_OFFSET_A0(sp)
        sw x11, STACK_OFFSET_A1(sp)
        sw x12, STACK_OFFSET_A2(sp)
        sw x13, STACK_OFFSET_A3(sp)
        sw x14, STACK_OFFSET_A4(sp)
        sw x15, STACK_OFFSET_A5(sp)
        sw x16, STACK_OFFSET_A6(sp)
        sw x17, STACK_OFFSET_A7(sp)
        sw x18, STACK_OFFSET_S2(sp)
        sw x19, STACK_OFFSET_S3(sp)
        sw x20, STACK_OFFSET_S4(sp)
        sw x21, STACK_OFFSET_S5(sp)
        sw x22, STACK_OFFSET_S6(sp)
        sw x23, STACK_OFFSET_S7(sp)
        sw x24, STACK_OFFSET_S8(sp)
        sw x25, STACK_OFFSET_S9(sp)
        sw x26, STACK_OFFSET_S10(sp)
        sw x27, STACK_OFFSET_S11(sp)
        sw x28, STACK_OFFSET_T3(sp)
        sw x29, STACK_OFFSET_T4(sp)
        sw x30, STACK_OFFSET_T5(sp)
        sw x31, STACK_OFFSET_T6(sp)
.endm

.macro pop_ctx
        lw x1, STACK_OFFSET_RA(sp)
        lw x3, STACK_OFFSET_GP(sp)
        lw x4, STACK_OFFSET_TP(sp)
        lw x5, STACK_OFFSET_T0(sp)
        lw x6, STACK_OFFSET_T1(sp)
        lw x7, STACK_OFFSET_T2(sp)
        lw x8, STACK_OFFSET_S0(sp)
        lw x9, STACK_OFFSET_S1(sp)
        lw x10, STACK_OFFSET_A0(sp)
        lw x11, STACK_OFFSET_A1(sp)
        lw x12, STACK_OFFSET_A2(sp)
        lw x13, STACK_OFFSET_A3(sp)
        lw x14, STACK_OFFSET_A4(sp)
        lw x15, STACK_OFFSET_A5(sp)
        lw x16, STACK_OFFSET_A6(sp)
        lw x17, STACK_OFFSET_A7(sp)
        lw x18, STACK_OFFSET_S2(sp)
        lw x19, STACK_OFFSET_S3(sp)
        lw x20, STACK_OFFSET_S4(sp)
        lw x21, STACK_OFFSET_S5(sp)
        lw x22, STACK_OFFSET_S6(sp)
        lw x23, STACK_OFFSET_S7(sp)
        lw x24, STACK_OFFSET_S8(sp)
        lw x25, STACK_OFFSET_S9(sp)
        lw x26, STACK_OFFSET_S10(sp)
        lw x27, STACK_OFFSET_S11(sp)
        lw x28, STACK_OFFSET_T3(sp)
        lw x29, STACK_OFFSET_T4(sp)
        lw x30, STACK_OFFSET_T5(sp)
        lw x31, STACK_OFFSET_T6(sp)

        addi sp, sp, 140 // 35 * 4
.endm
